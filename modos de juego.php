<h6>El juego actualmente se puede jugar en los siguientes modos:
<br>
<p style ="color:red">Tutorial:</p> Explica los controles y objetivos del juego para los jugadores principiantes.
<br>
<p style ="color:red">Cooperativo vs. IA:</p> Los jugadores compiten contra un equipo formado por bots (personajes controlados por la computadora).
<br>
<p style ="color:red">Entrenamiento:</p> Permite al jugador crear una partida de entrenamiento dónde los recursos, campeones, enfriamientos de habilidades y de los hechizos de invocador y la regeneración de los monstruos neutrales son controlados por él. Se utiliza para practicar la jugabilidad de cada campeón(Combos, objetos...) y el como usarlo en cada situación.
<br>
<p style ="color:red">ARAM:</p> (Siglas de All Random, All Mid, «Todos Aleatorio, Todos Medio» en español.) Dos equipos de cinco integrantes compiten con el objetivo de destruir el nexo del equipo rival en un mapa de un solo carril. La selección de campeón es aleatoria y por lo tanto puede presentarse un desequilibrio entre bandos.
<br>
<p style ="color:red">Modo personalizado:.</p> Permite a los jugadores crear manualmente y unirse a sesiones de juegos personalizados.
<br>
<p style ="color:red">Clasificatorias:.</p> Solo disponible para los jugadores con nivel 30 o superior. Este modo de juego presenta características especiales, por ejemplo, al finalizar cada partida se dan o quitan puntos de liga a los jugadores, clasificándolos en un rango en línea (de inferior a superior nivel: hierro, bronce, plata, oro, platino, diamante, maestro, gran maestro y retador). Además, cada equipo puede bloquear a cinco campeones, ver los campeones que está seleccionando el equipo rival (siendo las selecciones por turnos) y no se permiten repetir campeones. Este es el modo de juego usado en competiciones profesionales.
<br>
<p style ="color:red">Normales:.</p> Disponibles a partir de nivel 3, son partidas que las personas utilizan para divertirse, jugar con amigos o practicar y probar campeones. Se pueden jugar en la Grieta del invocador, y en el Bosque Retorcido. Todas las partidas especiales por eventos o rotación de modos de juego, incluido ARAM; son normales. No clasifican ni se obtiene rango, pero si Experiencia.
<br>
<p style ="color:red">URF: .</p> Es un modo de juego en la Grieta del invocador en el cual se obtiene la "Mejora increíble de la increíble increíblemente mejora" que concede:
100% de reducción de costos de mana/energía.
80% reducción de enfriamiento (no se puede aumentar con ítems).
+60 de velocidad de movimiento.
Duplica la velocidad de ataque para campeones de rango.
25% más de daño crítico para campeones de rango.
25% de tenacidad.
35% resistencia contra monstruos.
<br>
<p style ="color:red">ARURF:.</p> Consta del mismo modo de juego que en URF, pero la selección de campeones es el de ARAM.
<br>
Los objetos a comprar durante la partida y los hechizos de invocador disponibles varían de un mapa a otro, así como las características de la jungla.
<br>
El sistema de emparejamiento determina a quiénes se enfrentará un determinado jugador, sobre la base de las estadísticas de tal jugador (partidas ganadas y partidas perdidas). El emparejamiento también toma en cuenta la estadística media de los jugadores del equipo.</h6>
