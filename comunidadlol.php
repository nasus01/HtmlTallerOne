<h6>La moderación se llevaba a cabo a través de un sistema democrático conocido como el Tribunal. En este sistema, los jugadores que presentaban informes (aquellos reportados) eran revisados por otros jugadores en una base de caso por caso. Los jugadores debían revisar y luego presentar sus opiniones sobre la legalidad del comportamiento demostrado. Un consenso hacía a la decisión final. Es notable que los jugadores no eran capaces de prohibir permanentemente a través de este sistema, ya que «todas las prohibiciones permanentes se distribuyen de forma manual». Los revisores recibían una calificación de justicia sobre la base de su exactitud, en caso de elegir la opción correcta eran premiados con puntos de influencia. En la actualidad, este sistema se encuentra desactivado.
<br>
Actualmente la moderación se lleva con un sistema de comprobación de reportes (no se sabe si existe automatización). Cuando un jugador presenta una actitud poco deseable en una partida (insultos, inactividad intencionada) si es reportado, al cabo de un corto periodo de tiempo tras la partida (del orden de 15 minutos), podrá sufrir graves penalizaciones en su cuenta, como una suspensión total de 14 días o la inhabilitación permanente de su cuenta (nunca más podrá acceder a ella), con la consiguiente pérdida de todo el contenido (de pago o no) adquirido, sin ninguna posibilidad de recuperarlo, ni el propio contenido ni parte del dinero que empleó en conseguirlo.
<br>
Actualmente se esta llevando a cabo el chat de voz entre el equipo, aunque sea opcional y solo con compañeros invitados previamente a la partida.
</h6>
