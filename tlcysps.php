<h6>Cada campeón (personaje del juego) tiene habilidades y características y clases definidas por el juego, como en un juego de rol. Son seis clases de campeones:
<br>
<p style ="color:red">Luchador:</p>  Especializado en combate cercano. Combinan objetos de daño con objetos de aguante. También se los conoce como "offtanks"
<br>
<p style ="color:red">Tirador:</p> Especializado en ataque a distancia. Popularmente conocido como "AD Carry" del inglés attack damage carry o carreador de daño de ataque.
<br>
<p style ="color:red">Mago o Hechicero:</p> Especializado en habilidades y daño mágico. Popularmente conocido como "AP Carry" del inglés ability power carry o carreador de poder de habilidad.
<br>
<p style ="color:red">Asesino:</p> Especializado en emboscar al enemigo, atacando por sorpresa y retirándose rápidamente. Su misión es eliminar al jugador más importante del equipo enemigo (generalmente el tirador) aunque ello suponga morir él también.
<br>
<p style ="color:red">Tanque:</p> Especializado en resistencia. Su misión es absorber la mayor cantidad de daño posible e iniciar las peleas.
<br>
<p style ="color:red">Apoyo o Soporte:</p> Especializado en apoyar a su equipo y aportar visión en el mapa mediante guardianes de visión (wards).
<br>
<p style ="color:red">Posiciones:</p> Las posiciones de juego es el carril al que irá un jugador con su campeón. En La Grieta del Invocador, el escenario más común, las posiciones son:
<br>
<p style ="color:red">Carril superior.</p> Es el carril más cercano al Barón Nashor, una criatura neutral en la jungla que otorga bonificaciones para todo el equipo pero no de forma permanente. El Barón es un objetivo que aparece al minuto 20:00. Normalmente se necesita más de un jugador para matarlo. Donde usualmente juega el luchador o un tanque para complementar al equipo. También se suelen ver magos o incluso asesinos por temas de composición de equipo.
<br>
<p style ="color:red">Jungla. </p> Es la zona de criaturas neutrales entre los carriles. La clase del campeón en la jungla puede ser muy diversa, siempre que resista los ataques de las criaturas neutrales y sea eficiente asaltando carriles por sorpresa, por lo que suele jugarse un asesino resistente o un guerrero con buen movimiento.
<br>
<p style ="color:red">Carril central.</p> Es el carril rodeado por la jungla y camino más corto hacia la base enemiga, por lo que puede recibir gran cantidad de asaltos para asegurar una victoria rápida. Por ello usualmente se juega un mago que, por el daño mágico, puede responder a un ataque sorpresa, defender bajo torreta usando un asesino y cuando tiene ventaja asistir el ataque en otra línea; también se juegan asesinos para perjudicar al mago y atacar otras líneas.
<br>
<p style ="color:red">Carril inferior.</p> Es el carril cercano al dragón, una criatura neutral en la jungla que da beneficios a todo el equipo que lo mata. Dado que el dragón es un objetivo de equipo, en el carril inferior se juegan con dos personajes: el tirador, quien proporciona el daño a distancia para el equipo, y el support (soporte), para apoyar al tirador, ya que este suele ser poco resistente y es importante mantenerlo con vida.
<br>
Los objetos a comprar durante la partida y los hechizos de invocador disponibles varían de un mapa a otro, así como las características de la jungla.
<br>
El sistema de emparejamiento determina a quiénes se enfrentará un determinado jugador, sobre la base de las estadísticas de tal jugador (partidas ganadas y partidas perdidas). El emparejamiento también toma en cuenta la estadística media de los jugadores del equipo.</h6>
